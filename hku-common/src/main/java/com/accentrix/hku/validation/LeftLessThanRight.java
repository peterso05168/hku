package com.accentrix.hku.validation;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import javax.validation.Constraint;
import javax.validation.Payload;

import com.accentrix.hku.validation.validator.LeftLessThanRightValidator;

@Target(TYPE)
@Retention(RUNTIME)
@Documented
@Constraint(validatedBy = { LeftLessThanRightValidator.class })
public @interface LeftLessThanRight {

    String left();

    String right();

    String message() default "Left value MUST less than right value";

    Class<?>[] groups() default {};

    Class<? extends Payload>[] payload() default {};

    @Target(TYPE)
    @Retention(RUNTIME)
    @Documented
    @interface List {
        LeftLessThanRight[] value();
    }
}
